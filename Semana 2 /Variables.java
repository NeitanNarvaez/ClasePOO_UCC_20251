public class Variables {
    public static void main(String[] args) {
        //Declaracion de una Variables de tipo entero
        int edad = 25;
        //Declaracion de una variable de tipo real 
        double altura = 1.75;
        //Declaracion de una variable de cadena    
        String nombre = "Juan";
        //Declaracion de una variable de tipo boolean
        Boolean esEstudiante = true;
        /*
        Con la intruccion System.out.println se Mostrara las variables y una de ellas estara concatenada con el signo (+) y la leyenda 
        */
        System.out.println("nombre : " + nombre + ", edad : "+ edad +", altura: "+ altura);
        System.out.println("Estudiante "+ esEstudiante);

    }
}